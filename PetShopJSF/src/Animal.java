import java.util.Date;

public class Animal {

	private long id;
	private String nome;
	private Date nascimento;
	private float peso;

	public Animal(long id, String nome, Date nascimento, float peso) {

		this.id = id;
		this.nome = nome;
		this.nascimento = nascimento;
		this.peso = peso;

	}

	
	
}
